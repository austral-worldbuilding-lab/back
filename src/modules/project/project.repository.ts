  async update(
    id: string,
    updateProjectDto: UpdateProjectDto,
  ): Promise<ProjectDto> {
    if (
      'parentProjectId' in updateProjectDto &&
      updateProjectDto.parentProjectId !== undefined
    ) {
      throw new BadRequestException('Reparenting is not allowed');
    }

    if (
      'rootProjectId' in updateProjectDto &&
      updateProjectDto.rootProjectId !== undefined
    ) {
      throw new BadRequestException('rootProjectId is not updatable via API');
    }

    const updateData: {
      name?: string;
      description?: string;
      organizationId?: string;
      configuration?: Prisma.InputJsonValue;
    } = {};

    if (updateProjectDto.name !== undefined) {
      updateData.name = updateProjectDto.name;
    }

    if (updateProjectDto.description !== undefined) {
      updateData.description = updateProjectDto.description;
    }

    if (updateProjectDto.organizationId !== undefined) {
      updateData.organizationId = updateProjectDto.organizationId;
    }

    if (
      updateProjectDto.dimensions !== undefined ||
      updateProjectDto.scales !== undefined
    ) {
      const currentProject = await this.prisma.project.findUnique({
        where: { id },
        select: { configuration: true },
      });

      if (!currentProject) {
        throw new Error(`Project with id ${id} not found`);
      }

      const currentConfig = this.parseToProjectConfiguration(
        currentProject.configuration,
      );

      updateData.configuration = this.parseToJson({
        dimensions: updateProjectDto.dimensions ?? currentConfig.dimensions,
        scales: updateProjectDto.scales ?? currentConfig.scales,
      });
    }

    const project = await this.prisma.project.update({
      where: { id },
      data: updateData,
    });

    return this.parseToProjectDto(project);
  }

